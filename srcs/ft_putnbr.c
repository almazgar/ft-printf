#include "ft_printf.h"

void	ft_putnbr(__int128_t n, t_print *ssl)
{
	if (n == -2147483648)
	{
		ft_putstr("-2147483648", ssl);
		return ;
	}
	if (n < 0)
	{
		n = -n;
		ft_putchar('-', ssl);
	}
	if (n > 9)
		ft_putnbr(n / 10, ssl);
	ft_putchar(n % 10 + '0', ssl);
}
